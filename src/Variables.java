// 02 Variables: Definicion de variables de tipo entero y cadena
public class Variables {
    public static void main( String args[] ) throws Exception {

        //  Define variables de tipo entero
        int dia = 13;
        int anio = 1996;
        //  Define variables de tipo cadena
        String mes = "enero";
        String mensaje = "Fecha del lanzamiento inicial de Java: ";

        dia = 23;

        System .out .println( mensaje + dia + " de " + mes + " de " + anio );

    }
}
